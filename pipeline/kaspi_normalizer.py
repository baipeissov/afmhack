"""
Нормализатор зашифрованных номеров Kaspi. Мошенники в прямых эфирах
произносят/пишут номер словами и буквами ("восемь семь семь семь"),
иногда разбивая слово лишними пробелами ("дв а три"), чтобы обойти простой
regex на \\d{11}. Переводим слова обратно в цифры и ищем казахстанский
мобильный номер (7/8 + 10 цифр) в получившейся цифровой строке.

Ограничения (осознанно, ради времени на хакатоне):
  - не разбираем составные числительные ("семьдесят", "сто") — мошенники
    обычно произносят номер по одной цифре, а не как число целиком;
  - не подменяем кириллические буквы, похожие на цифры (о->0, з->3) —
    слишком высокий риск ложных срабатываний на обычном тексте без
    отдельной модели уверенности.
"""

import re

NUMBER_WORDS = {
    # русский
    "ноль": "0", "нуль": "0",
    "один": "1", "одна": "1",
    "два": "2",
    "три": "3",
    "четыре": "4",
    "пять": "5",
    "шесть": "6",
    "семь": "7",
    "восемь": "8",
    "девять": "9",
    # казахский
    "нөл": "0",
    "бір": "1",
    "екі": "2",
    "үш": "3",
    "төрт": "4",
    "бес": "5",
    "алты": "6",
    "жеті": "7",
    "сегіз": "8",
    "тоғыз": "9",
    # римские цифры (иногда используют как замену цифрам на экране)
    "ix": "9", "viii": "8", "vii": "7", "vi": "6", "v": "5",
    "iv": "4", "iii": "3", "ii": "2", "i": "1",
}

# длинные слова первыми — иначе "vi" словит часть "viii" раньше времени
_SORTED_WORDS = sorted(NUMBER_WORDS.items(), key=lambda kv: -len(kv[0]))


def _spaced_pattern(word: str) -> str:
    """'два' -> паттерн, ловящий 'два', 'дв а', 'д в а' — разбивку лишними
    пробелами, которой шифруют слово от простого regex на цифры."""
    return r"\s*".join(re.escape(letter) for letter in word)


_PATTERNS = [
    (
        re.compile(rf"(?<![а-яёa-z]){_spaced_pattern(word)}(?![а-яёa-z])", re.IGNORECASE),
        digit,
    )
    for word, digit in _SORTED_WORDS
]


def normalize_spoken_digits(text: str) -> str:
    """Заменяет словесные/разбитые-пробелами цифры на цифры. Остальной
    текст не трогаем — он нужен как контекст фразы для отчёта аналитику."""
    result = text.lower()
    for pattern, digit in _PATTERNS:
        result = pattern.sub(digit, result)
    return result


# казахстанский мобильный номер: 7 или 8 + 10 цифр
_KASPI_PHONE_RE = re.compile(r"[78]\d{9}")
# последовательность цифр (после нормализации слов), разделённых только
# пробелами/дефисами — именно во что превращается номер, произнесённый
# по одной цифре
_DIGIT_RUN_RE = re.compile(r"(?:\d[\s\-]*){7,}")


def find_kaspi_numbers(text: str) -> list[str]:
    """Ищет казахстанские номера в тексте после нормализации словесного
    шифрования. Возвращает список уникальных номеров (порядок сохранён)."""
    normalized = normalize_spoken_digits(text)
    found = []
    for run in _DIGIT_RUN_RE.finditer(normalized):
        digits_only = re.sub(r"\D", "", run.group())
        for phone in _KASPI_PHONE_RE.finditer(digits_only):
            found.append(phone.group())

    seen = set()
    unique = []
    for number in found:
        if number not in seen:
            seen.add(number)
            unique.append(number)
    return unique


if __name__ == "__main__":
    examples = [
        "звоните на каспи восемь семь семь семь один два три четыре пять шесть семь",
        "перевод на восемь дв а три четыре пять шесть семь восемь девять ноль",
        "у нас отличный сервис, три магазина в городе",  # контроль: не должно сработать
    ]
    for ex in examples:
        print(f"text: {ex}")
        print(f"  normalized: {normalize_spoken_digits(ex)}")
        print(f"  found: {find_kaspi_numbers(ex)}")
